"""Pydantic models for skill manifest parsing and validation."""

import re

from pydantic import BaseModel, Field, field_validator, model_validator
from typing import Literal, Optional


_BASH_PATTERN_RE = re.compile(r"^Bash\(([^)]+):\*\)$")
_REQUIRES_KEY_RE = re.compile(r"^[a-z][a-z0-9_]*$")


def _validate_bash_entries(entries: list[str]) -> list[str]:
    """Reject bare 'Bash' and malformed Bash() patterns.

    Allowed forms:
        - Non-Bash tool names (passed through unchanged)
        - 'Bash(*)' — explicit wildcard
        - 'Bash(<prefix>:*)' — prefix-restricted

    Raises:
        ValueError if a bare 'Bash' or malformed 'Bash(...)' entry is found.
    """
    for entry in entries:
        if entry == "Bash":
            raise ValueError(
                "bare 'Bash' is not allowed; use 'Bash(*)' for wildcard "
                "or 'Bash(<prefix>:*)' to restrict commands"
            )
        if entry.startswith("Bash(") and entry != "Bash(*)":
            if not _BASH_PATTERN_RE.match(entry):
                raise ValueError(
                    f"invalid Bash pattern {entry!r}; expected 'Bash(<prefix>:*)' "
                    "or 'Bash(*)'"
                )
    return entries


class SkillMetadata(BaseModel):
    """Skill metadata section."""

    name: str
    version: str
    author: Optional[str] = None
    description: Optional[str] = None
    tags: list[str] = Field(default_factory=list)


class VolumeMount(BaseModel):
    """Volume mount configuration for containers."""

    host: str  # Host path (e.g., ~/.claude/projects)
    mode: Literal["ro", "rw"] = "ro"  # Mount mode
    # Container path is auto-generated by executor as /home/agent/workspace/<server-name>


class SkillMount(BaseModel):
    """Generic bind mount declared at spec.mounts level (independent of MCP).

    Two forms:
      - Static: set `host` + `container`. host expanded at run time.
      - Dynamic: set `source` (e.g. 'requires.project_roots') + ONE of:
          - `container` (single directory)
          - `container_prefix` (list[directory] expanded to one mount per
            item; container path = prefix + basename)
          - `preserve_host_path: true` (each value mounted at the SAME
            absolute path inside the container as on the host — useful
            when downstream tools embed host paths, e.g. Claude session
            JSONL `cwd` fields for git resolution by agenthud --with-git)
    """

    host: Optional[str] = None
    source: Optional[str] = None
    container: Optional[str] = None
    container_prefix: Optional[str] = None
    preserve_host_path: bool = False
    mode: Literal["ro", "rw"] = "ro"

    @field_validator("container")
    @classmethod
    def _container_must_be_absolute(cls, v: Optional[str]) -> Optional[str]:
        if v is not None and not v.startswith("/"):
            raise ValueError(
                f"container path must be absolute (starts with '/'), got {v!r}"
            )
        return v

    @field_validator("container_prefix")
    @classmethod
    def _container_prefix_rules(cls, v: Optional[str]) -> Optional[str]:
        if v is None:
            return v
        if not v.startswith("/"):
            raise ValueError(
                f"container_prefix must be absolute (starts with '/'), got {v!r}"
            )
        if not v.endswith("/"):
            raise ValueError(
                f"container_prefix must end with '/', got {v!r}"
            )
        return v

    @field_validator("source")
    @classmethod
    def _source_must_reference_requires(cls, v: Optional[str]) -> Optional[str]:
        if v is not None and not v.startswith("requires."):
            raise ValueError(
                f"source must start with 'requires.', got {v!r}"
            )
        return v

    @model_validator(mode="after")
    def _check_field_combinations(self) -> "SkillMount":
        # host XOR source
        if self.host is not None and self.source is not None:
            raise ValueError("'host' and 'source' are mutually exclusive")
        if self.host is None and self.source is None:
            raise ValueError("must set either 'host' (static) or 'source' (dynamic)")

        # preserve_host_path requires source (only applies to dynamic mounts)
        if self.preserve_host_path and self.source is None:
            raise ValueError(
                "preserve_host_path requires 'source' (only valid on dynamic mounts)"
            )

        # preserve_host_path cannot be combined with container/container_prefix
        if self.preserve_host_path and (
            self.container is not None or self.container_prefix is not None
        ):
            raise ValueError(
                "preserve_host_path cannot be combined with 'container' or "
                "'container_prefix' (the container path IS the host path)"
            )

        # container XOR container_prefix (when not using preserve_host_path)
        if not self.preserve_host_path:
            if self.container is not None and self.container_prefix is not None:
                raise ValueError("container and container_prefix are mutually exclusive")
            if self.container is None and self.container_prefix is None:
                raise ValueError("must set either 'container' or 'container_prefix'")

        # Static mounts can't use container_prefix
        if self.host is not None and self.container_prefix is not None:
            raise ValueError("static mounts (host) cannot use container_prefix")

        return self


class MCPServerStdio(BaseModel):
    """Stdio MCP server configuration."""

    name: str
    type: Literal["stdio"]
    command: str  # Command to run (npx, uvx, python, etc.)
    args: list[str]  # Command arguments
    mount: Optional[VolumeMount] = None
    env: list[str] = Field(default_factory=list)  # Required environment variables
    allowed_tools: list[str] = Field(default_factory=list)  # Tool whitelist (empty = all)


class MCPServerAuth(BaseModel):
    """OAuth authentication configuration for HTTP MCP servers."""

    type: Literal["oauth2", "none"]


class MCPServerHTTP(BaseModel):
    """HTTP MCP server configuration."""

    name: str
    type: Literal["http"]
    url: str  # HTTP endpoint URL
    connection: Optional[str] = None  # Connection name
    headersHelper: Optional[str] = None  # Shell command to generate headers
    env: list[str] = Field(default_factory=list)  # Required environment variables
    allowed_tools: list[str] = Field(default_factory=list)  # Tool whitelist (empty = all)
    auth: Optional[MCPServerAuth] = None  # Authentication configuration


# Union type for MCP servers
MCPServer = MCPServerStdio | MCPServerHTTP


class SkillTools(BaseModel):
    """Tool whitelist configuration."""

    builtin: list[str] = Field(default_factory=list)  # Built-in Claude Code tools

    @field_validator("builtin")
    @classmethod
    def _check_bash_pattern(cls, v: list[str]) -> list[str]:
        return _validate_bash_entries(v)


class SkillLimits(BaseModel):
    """Resource limits for skill execution."""

    max_turns: Optional[int] = None
    max_cost_usd: Optional[float] = None
    timeout_seconds: Optional[int] = None


class RequiresEntry(BaseModel):
    """One per-user, per-skill host-side value the launcher must obtain
    before starting the container.

    Declared in spec.requires.<key>; values stored in
    ~/.zipsa/<skill>@<version>/requires.yaml.
    """

    type: Literal["string", "directory", "list[directory]"]
    prompt: str

    @field_validator("prompt")
    @classmethod
    def _prompt_not_empty(cls, v: str) -> str:
        if not v.strip():
            raise ValueError("prompt must be non-empty")
        return v


class PhaseSpec(BaseModel):
    """Single phase declaration within a multi-phase skill."""

    id: str
    goal: str
    allowed_tools: list[str] = Field(default_factory=list)
    limits: Optional[SkillLimits] = None
    # Per-phase model override. Falls back to spec.model when None.
    # Same shape as SkillSpec.model: {"name": "claude-haiku-4-5-..."}.
    model: Optional[dict] = None

    @field_validator("allowed_tools")
    @classmethod
    def _check_bash_pattern(cls, v: list[str]) -> list[str]:
        return _validate_bash_entries(v)


class SkillSpec(BaseModel):
    """Skill specification."""

    purpose: str  # Skill purpose description
    instructions: str  # Path to SKILL.md
    model: Optional[dict] = None  # Model configuration
    tools: SkillTools = Field(default_factory=SkillTools)
    mcp: list[MCPServer] = Field(default_factory=list)
    mounts: list[SkillMount] = Field(default_factory=list)  # Generic bind mounts
    limits: Optional[SkillLimits] = None
    config: dict = Field(default_factory=dict)  # Skill-specific config
    requires: dict[str, RequiresEntry] = Field(default_factory=dict)  # Per-user host-side values
    network: Optional[dict] = None  # Network allow list
    default_query: Optional[str] = None  # Default query for skill (when user runs with no args)
    children: list[str] = Field(default_factory=list)  # Child skills this skill may invoke
    # Opt-in to invoke unsaved skills from ~/.zipsa/staging/ via
    # mcp__zipsa__run_staging_skill. Only privileged authoring meta-skills
    # (e.g. skill-builder) should set this — production skills should
    # operate on installed children only.
    allows_staging_run: bool = False
    # Multi-phase support (v1: documentation only, v2: strict validation)
    phases: list[PhaseSpec] = Field(default_factory=list)
    state_schema: dict = Field(default_factory=dict)  # v1: docs only

    @field_validator("children")
    @classmethod
    def _reject_path_traversal(cls, v: list[str]) -> list[str]:
        """Reject children entries that contain path traversal characters.

        Child names must be simple skill identifiers (e.g. 'daily-progress').
        Entries containing '/', '\\', or '..' are path traversal vectors and
        are rejected at validation time even though execution is sandboxed.
        """
        for entry in v:
            if "/" in entry or "\\" in entry or ".." in entry:
                raise ValueError(
                    f"children entry {entry!r} contains a path traversal sequence "
                    "('/', '\\\\', or '..'); use plain skill names only"
                )
        return v

    @field_validator("requires")
    @classmethod
    def _validate_requires_keys(cls, v: dict[str, RequiresEntry]) -> dict[str, RequiresEntry]:
        for key in v.keys():
            if not _REQUIRES_KEY_RE.match(key):
                raise ValueError(
                    f"requires key {key!r} must be lowercase letters/digits/"
                    "underscores and start with a letter"
                )
        return v

    @model_validator(mode="after")
    def _check_mount_requires_consistency(self) -> "SkillSpec":
        for i, mount in enumerate(self.mounts):
            if mount.source is None:
                continue
            key = mount.source.removeprefix("requires.")
            if key not in self.requires:
                raise ValueError(
                    f"mounts[{i}].source references unknown requires key: {key!r}"
                )
            entry = self.requires[key]
            if entry.type == "directory" and mount.container_prefix is not None:
                raise ValueError(
                    f"mounts[{i}]: use 'container' for single directory "
                    f"(requires.{key} has type=directory)"
                )
            if entry.type == "list[directory]" and mount.container is not None:
                raise ValueError(
                    f"mounts[{i}]: use 'container_prefix' for list "
                    f"(requires.{key} has type=list[directory])"
                )
            if entry.type == "string":
                raise ValueError(
                    f"mounts[{i}]: requires.{key} (type=string) cannot be used as a mount source"
                )
        return self


class SkillManifest(BaseModel):
    """Complete skill manifest."""

    apiVersion: str
    kind: Literal["SkillManifest"]
    metadata: SkillMetadata
    spec: SkillSpec
